  sum = num1.to_i - num2.to_i
  puts "#{sum}"
  return sum
end

subtraction(num1, num2)

def division(num1, num2)
  sum = num1.to_i / num2.to_i
  puts "#{sum}"
  return sum
end

division(num1, num2)

def multiplication(num1, num2)
  sum = num1.to_i * num2.to_i
  puts "#{sum}"
  return sum
end

multiplication(num1, num2)

